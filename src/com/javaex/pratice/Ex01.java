package com.javaex.pratice;

public class Ex01 {
	public static void main(String[]args) {
		System.out.println("while문");
		int index =0;
		while(index<3) {
			System.out.println("index= "+index);
			index++;
		}//while문 
		//index=0부터 1 2까지출력
	System.out.println("for문");
	for(int order=0; order<3; order++) {
		System.out.println("order=" +order);
	}
	//for문
	//order=0부터 1 2까지 출력
	}
	
}	
